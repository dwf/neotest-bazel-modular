-- Reconstruct the method names absl.testing generates from
-- @parameterized.named_parameters decorators, by parsing the test source with
-- the Python tree-sitter grammar.
--
-- absl expands `@parameterized.named_parameters` on `test_foo` into a real
-- method `test_foo_<casename>` per case.  The neotest tree only has the
-- decorated `test_foo`, so the collector must map `test_foo_<casename>` back to
-- `test_foo`.  Doing that by longest prefix is ambiguous when a sibling method's
-- name is itself a prefix of an expansion (e.g. `test_foo_na` vs `test_foo`'s
-- `nap` case -> `test_foo_nap`).  Reconstructing the exact expansion names from
-- the decorator removes the guess: absl guarantees generated names are unique,
-- so `expansion -> method` is an exact, unambiguous map.
--
-- Handled case forms (the first element / testcase_name value must be a string
-- literal):
--   ("name", ...)                    -- tuple, name first
--   {"testcase_name": "Name", ...}   -- dict literal, single or double quotes
--   dict(testcase_name="Name", ...)  -- dict() call with a testcase_name kwarg
--
-- Out of scope: cases generated by comprehensions or other non-literal
-- expressions.  Building test names from a comprehension is an anti-pattern
-- (the names aren't greppable); such methods are simply not reconstructed and
-- fall back to the collector's prefix matching.

local M = {}

local function get_text(node, content)
  return vim.treesitter.get_node_text(node, content)
end

-- The value of a (string) node, or nil for non-string / non-literal nodes.
local function string_value(node, content)
  if not node or node:type() ~= "string" then
    return nil
  end
  for i = 0, node:named_child_count() - 1 do
    if node:named_child(i):type() == "string_content" then
      return get_text(node:named_child(i), content)
    end
  end
  -- Empty string ("") has no string_content child.
  local raw = get_text(node, content)
  return raw:match('^"(.*)"$') or raw:match("^'(.*)'$") or nil
end

-- First named child of `node` whose type is `kind`, or nil.
local function child_of_type(node, kind)
  for i = 0, node:named_child_count() - 1 do
    local c = node:named_child(i)
    if c:type() == kind then
      return c
    end
  end
  return nil
end

-- Extract the testcase name from one named_parameters argument, or nil when it
-- isn't one of the handled literal forms.
local function case_name(arg, content)
  local t = arg:type()
  if t == "tuple" then
    -- ("name", ...): the first element is the name.
    return string_value(arg:named_child(0), content)
  elseif t == "dictionary" then
    -- {"testcase_name": "Name", ...}
    for i = 0, arg:named_child_count() - 1 do
      local pair = arg:named_child(i)
      if pair:type() == "pair" then
        local k = string_value(pair:named_child(0), content)
        if k == "testcase_name" then
          return string_value(pair:named_child(1), content)
        end
      end
    end
  elseif t == "call" then
    -- dict(testcase_name="Name", ...)
    local fn = arg:named_child(0)
    if fn and get_text(fn, content) == "dict" then
      local args = child_of_type(arg, "argument_list")
      if args then
        for i = 0, args:named_child_count() - 1 do
          local kw = args:named_child(i)
          if kw:type() == "keyword_argument" then
            local name = kw:named_child(0)
            if name and get_text(name, content) == "testcase_name" then
              return string_value(kw:named_child(1), content)
            end
          end
        end
      end
    end
  end
  return nil
end

-- Given a decorated_definition node, return (method_name, named_parameters
-- argument_list) if it carries a named_parameters decorator, else nil.
local function named_parameters_of(decorated, content)
  local method, args
  for i = 0, decorated:named_child_count() - 1 do
    local c = decorated:named_child(i)
    if c:type() == "decorator" then
      local call = c:named_child(0)
      if call and call:type() == "call" then
        local fn = call:named_child(0)
        if fn and get_text(fn, content):match("named_parameters$") then
          args = child_of_type(call, "argument_list")
        end
      end
    elseif c:type() == "function_definition" then
      local name = child_of_type(c, "identifier")
      if name then
        method = get_text(name, content)
      end
    end
  end
  if method and args then
    return method, args
  end
end

-- Walk the tree, tracking the enclosing class, collecting expansions.
local function walk(node, content, class, out)
  local t = node:type()
  if t == "class_definition" then
    local name = child_of_type(node, "identifier")
    local class_name = name and get_text(name, content) or class
    for i = 0, node:named_child_count() - 1 do
      walk(node:named_child(i), content, class_name, out)
    end
    return
  end
  if t == "decorated_definition" and class then
    local method, args = named_parameters_of(node, content)
    if method then
      out[class] = out[class] or {}
      for i = 0, args:named_child_count() - 1 do
        local name = case_name(args:named_child(i), content)
        if name then
          out[class][method .. "_" .. name] = method
        end
      end
    end
    return
  end
  for i = 0, node:named_child_count() - 1 do
    walk(node:named_child(i), content, class, out)
  end
end

-- Parse `path` and return { [class_name] = { [expansion_name] = method_name } },
-- or an empty table when the file can't be read or parsed.
function M.named_expansions(path)
  local out = {}
  local f = io.open(path, "r")
  if not f then
    return out
  end
  local content = f:read("*a")
  f:close()

  local ok, parser = pcall(vim.treesitter.get_string_parser, content, "python")
  if not ok then
    return out
  end
  local tree = parser:parse()[1]
  if not tree then
    return out
  end
  walk(tree:root(), content, nil, out)
  return out
end

return M
